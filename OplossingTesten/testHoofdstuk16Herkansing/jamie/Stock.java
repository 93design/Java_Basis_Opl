package jamie;

public class Stock {
	
	private int appleSourAmount;
	private int appleSweetAmount;
	private int orangeAmount;
	private int straweberryAmount;
	private int spinageAmount;
	private int celeryAmount;
	
	public Stock(int appleSourAmount, int appleSweetAmount, int orangeAmount, int straweberryAmount, int spinageAmount,
			int celeryAmount) {
		super();
		this.appleSourAmount = appleSourAmount;
		this.appleSweetAmount = appleSweetAmount;
		this.orangeAmount = orangeAmount;
		this.straweberryAmount = straweberryAmount;
		this.spinageAmount = spinageAmount;
		this.celeryAmount = celeryAmount;
	}
	
	
}
